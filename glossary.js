/* ───────── TERM SCANNER ───────── */
// Sort terms longest first so "Browser Extension" matches before "Browser"
var terms = Object.keys(G).sort(function(a,b){ return b.length - a.length; });

// Build a giant regex with word boundaries for English terms
var reStr = terms.map(function(t){
  // For CJK or mixed, just match the string; for pure ASCII, use word boundary
  if(/^[A-Za-z0-9\s\-\.\/]+$/.test(t)){
    return '\\b' + t.replace(/[.*+?^${}()|[\]\\]/g,'\\$&') + '\\b';
  }
  return t.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
}).join('|');
var re = new RegExp('(' + reStr + ')', 'g');

function wrapTerms(el){
  var walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT, null, false);
  var nodes = [];
  while(walker.nextNode()) nodes.push(walker.currentNode);

  nodes.forEach(function(node){
    // Skip if parent is already a term, a script, style, or heading
    var p = node.parentNode;
    if(!p || p.classList && p.classList.contains('g-term')) return;
    var tag = p.tagName;
    if(tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT' || tag === 'TEXTAREA' || tag === 'INPUT') return;
    // Skip ticker (too noisy)
    if(p.closest && p.closest('.ticker-inner')) return;
    // Skip table header
    if(tag === 'TH') return;

    var text = node.nodeValue;
    if(!re.test(text)){ return; }
    re.lastIndex = 0;

    var frag = document.createDocumentFragment();
    var lastIdx = 0;
    var match;
    re.lastIndex = 0;
    while((match = re.exec(text)) !== null){
      // Add text before match
      if(match.index > lastIdx){
        frag.appendChild(document.createTextNode(text.slice(lastIdx, match.index)));
      }
      // Create tooltip span
      var term = match[1];
      // Find the glossary entry (case-insensitive match)
      var entry = G[term];
      if(!entry){
        // Try case-insensitive lookup
        for(var k in G){ if(k.toLowerCase() === term.toLowerCase()){ entry = G[k]; break; } }
      }
      if(entry){
        var span = document.createElement('span');
        span.className = 'g-term';
        span.textContent = term;
        var tip = document.createElement('span');
        tip.className = 'g-tip';
        tip.innerHTML = '<span class="g-word">' + term + '</span>' +
          '<span class="g-mean">' + entry.m + '</span>' +
          '<span class="g-ex">💡 ' + entry.e + '</span>';
        span.appendChild(tip);
        frag.appendChild(span);
      } else {
        frag.appendChild(document.createTextNode(term));
      }
      lastIdx = re.lastIndex;
    }
    // Add remaining text
    if(lastIdx < text.length){
      frag.appendChild(document.createTextNode(text.slice(lastIdx)));
    }
    p.replaceChild(frag, node);
  });
}

/* ───────── MOBILE TOUCH SUPPORT ───────── */
document.addEventListener('click', function(e){
  // Close all open tooltips
  var openTerms = document.querySelectorAll('.g-term.g-open');
  openTerms.forEach(function(t){ t.classList.remove('g-open'); });

  // If clicked on a term, toggle it
  var term = e.target.closest('.g-term');
  if(term){
    e.preventDefault();
    term.classList.add('g-open');
  }
});

/* ───────── TOOLTIP POSITION FIX ───────── */
// If tooltip goes off-screen top, show below instead
function fixPosition(){
  document.querySelectorAll('.g-term:hover .g-tip, .g-term.g-open .g-tip').forEach(function(tip){
    var rect = tip.getBoundingClientRect();
    if(rect.top < 0){
      tip.style.bottom = 'auto';
      tip.style.top = 'calc(100% + 8px)';
      tip.querySelector('::after') // Can't easily flip arrow in pure JS, but position is fixed
    }
  });
}
document.addEventListener('mouseover', fixPosition);

/* ───────── INIT ───────── */
// Wait for page to load, then scan
function init(){
  // Scan index view
  var indexView = document.getElementById('index-view');
  if(indexView) wrapTerms(indexView);

  // Scan detail view content on showDetail
  var origShowDetail = window.showDetail;
  if(origShowDetail){
    window.showDetail = function(id){
      origShowDetail(id);
      setTimeout(function(){
        var dc = document.getElementById('detail-content');
        if(dc) wrapTerms(dc);
      }, 50);
    };
  }

  // If no index-view (different page structure), scan body
  if(!indexView){
    wrapTerms(document.body);
  }
}

if(document.readyState === 'loading'){
  document.addEventListener('DOMContentLoaded', init);
} else {
  init();
}

